void main() {
  print('');
  print(
      "Q1: Write a program to find the factorial of any number entered using for loop.");
  int num = 5;
  int factorial = 1;
  for (var i = 1; i <= num; i++) {
    factorial *= i;
    //factorial = i * num;
  }
  print("Factorial of $num is $factorial");
